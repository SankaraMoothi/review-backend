import mongoose from "mongoose";

const ReviewSchema = new mongoose.Schema(
  {
    userName: {
      type: String,
      required: true,
    },
    title: {
      type: String,
      required: true,
    },
    content: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

export default mongoose.model("review", ReviewSchema);
